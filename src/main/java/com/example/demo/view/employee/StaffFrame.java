    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        boolean result = controller.cancelDayOff(user.getId());
        
        if(result){
            submitButton.setEnabled(true);
            cancelButton.setEnabled(false);
            statusLabel.setText("Chưa gửi");
            JOptionPane.showMessageDialog(this, "Hủy thành công");
        } else{
            JOptionPane.showMessageDialog(this, "Hủy thất bại");
        }
    }//GEN-LAST:event_cancelButtonActionPerformed

    private void submitDayOff() {
        DayOff dayOff = new DayOff();
        
        try {
            LocalDate date = LocalDate.parse(dateText.getText());
            dayOff.setDate(date);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Nhập theo định dạng yyyy-mm-dd");
            return;
        }
        
        try {
            float number = Float.parseFloat(numberDayOffText.getText());
            
            assert number > 0f;
            
            dayOff.setNumberDay(number);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập vào số");
            return;
        }
        
        boolean result = controller.submitDayOff(user.getId(), dayOff);
        if(result){
            submitButton.setEnabled(false);
            cancelButton.setEnabled(true);
            statusLabel.setText("Đã gửi");
            JOptionPane.showMessageDialog(this, "Thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        
    }

