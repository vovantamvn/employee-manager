    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        // TODO add your handling code here:
        
        String p1 = pass1.getText();
        String p2 = pass2.getText();
        
        if(!p1.equals(p2)){
            JOptionPane.showMessageDialog(this, "Password không khớp nhau");
            return;
        }
        
        boolean result = controller.changPassword(user, p1);
        
        if(result){
            JOptionPane.showMessageDialog(this, "Đã thay đổi mật khẩu");
        } else {
            JOptionPane.showMessageDialog(this, "Thay đổi mật khẩu không thành công");
        }
        
    }//GEN-LAST:event_changePasswordButtonActionPerformed

